#include "libft.h"

char	*ft_strndup(const char *s, int n)
{
	char	*dup;
	int		i;

	dup = ft_calloc(n + 1, sizeof(char));
	if (!dup)
		return (NULL);
	i = 0;
	while (i < n && s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
